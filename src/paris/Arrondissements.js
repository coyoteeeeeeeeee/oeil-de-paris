export function getArrondissement(lat, lng) {
  for (const arr of ARR_BORDERS) {
    if (pointInPolygon(lat, lng, arr.coords)) return arr
  }
  return null
}

function pointInPolygon(lat, lng, poly) {
  let inside = false
  for (let i = 0, j = poly.length - 1; i < poly.length; j = i++) {
    const [la_i, ln_i] = poly[i]
    const [la_j, ln_j] = poly[j]
    if (((ln_i > lng) !== (ln_j > lng)) &&
        (lat < (la_j - la_i) * (lng - ln_i) / (ln_j - ln_i) + la_i))
      inside = !inside
  }
  return inside
}